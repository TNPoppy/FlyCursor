void ssd1306_write_char(uint8_t ch)
{
    if (ch < ' ' || ch > 'z') return;
    uint8_t idx = ch - ' ';
    if (idx >= sizeof(font_5x8) / 5) return;
    uint8_t col_data[5];
    memcpy(col_data, &font_5x8[idx * 5], 5);
    ssd1306_send_data(col_data, 5);
}

void ssd1306_write_string(const char *str)
{
    while (*str) {
        ssd1306_write_char(*str++);
    }
}

void ssd1306_draw_bitmap(uint8_t x, uint8_t y, uint8_t width, uint8_t height, const uint8_t *bitmap)
{
    if (x >= SSD1306_WIDTH || y >= SSD1306_HEIGHT) return;

    for (uint8_t page = 0; page < (height + 7) / 8; page++) {
        uint8_t py = y / 8 + page;
        if (py >= SSD1306_MAX_PAGES) break;

        ssd1306_set_page_addr(py);
        ssd1306_set_column_addr(x);

        uint8_t line_width = width;
        if (x + line_width > SSD1306_WIDTH) {
            line_width = SSD1306_WIDTH - x;
        }

        uint8_t buf[64];
        for (uint8_t i = 0; i < line_width; i++) {
            uint8_t bitmap_idx = page * width + i;
            if (bitmap_idx < width * ((height + 7) / 8)) {
                buf[i] = bitmap[bitmap_idx];
            } else {
                buf[i] = 0;
            }
        }
        ssd1306_send_data(buf, line_width);
    }
}

void ssd1306_fill_screen(uint8_t fill)
{
    uint8_t data[128];
    memset(data, fill ? 0xFF : 0x00, sizeof(data));

    for (uint8_t page = 0; page < SSD1306_MAX_PAGES; page++) {
        ssd1306_set_page_addr(page);
        ssd1306_set_column_addr(0);
        ssd1306_send_data(data, sizeof(data));
    }
}

void ssd1306_test_pattern(void)
{
    for (uint8_t page = 0; page < SSD1306_MAX_PAGES; page++) {
        ssd1306_set_page_addr(page);
        ssd1306_set_column_addr(0);
        uint8_t buf[128];
        for (uint8_t i = 0; i < 128; i++) {
            buf[i] = (i % 2 == 0) ? 0xAA : 0x55;
        }
        ssd1306_send_data(buf, sizeof(buf));
    }
}
