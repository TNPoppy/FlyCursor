#include "ssd1306.h"
#include "esp_log.h"
#include "esp_err.h"
#include <string.h>

static const char *TAG = "SSD1306";

#define SSD1306_I2C_ADDR 0x3C
#define SSD1306_WIDTH 64
#define SSD1306_HEIGHT 32
#define SSD1306_MAX_PAGES 4
#define SSD1306_COL_OFFSET 32   // 64x32 小屏常显示在 128 列中的第 32~95 列

static i2c_master_dev_handle_t ssd1306_dev_handle;
static i2c_master_bus_handle_t ssd1306_i2c_bus_handle;

static const uint8_t ssd1306_init_sequence[] = {
    0xAE,       // Display OFF
    0xD5, 0x80, // Set Display Clock Divide Ratio / OSC Frequency
    0xA8, 0x1F, // Set Multiplex Ratio (32 rows -> 0x1F)
    0xD3, 0x00, // Set Display Offset = 0
    0x40,       // Set Display Start Line = 0
    0x8D, 0x14, // Charge Pump Setting (enable)
    0x20, 0x02, // Set Memory Addressing Mode (Page Addressing)
    0xA1,       // Set Segment Re-map (reverse)
    0xC8,       // Set COM Output Scan Direction (reverse)
    0xDA, 0x12, // Set COM Pins Hardware Configuration (Alternative, no remap)
    0x81, 0x8F, // Set Contrast Control (moderate)
    0xD9, 0xF1, // Set Pre-charge Period
    0xDB, 0x40, // Set VCOMH Deselect Level (0x40 = 0.77xVcc)
    0xA4,       // Entire Display ON (resume RAM)
    0xA6,       // Set Normal Display (non-inverted)
    0xAF,       // Display ON
};

static void ssd1306_send_cmd(uint8_t cmd)
{
    uint8_t data[2] = {0x00, cmd};
    ESP_ERROR_CHECK(i2c_master_transmit(ssd1306_dev_handle, data, 2, -1));
}

void ssd1306_send_data(const uint8_t *data, size_t len)
{
    uint8_t *buf = malloc(len + 1);
    if (buf == NULL) {
        ESP_LOGE(TAG, "Memory allocation failed");
        return;
    }
    buf[0] = 0x40;
    memcpy(&buf[1], data, len);
    ESP_ERROR_CHECK(i2c_master_transmit(ssd1306_dev_handle, buf, len + 1, -1));
    free(buf);
}

static void ssd1306_set_page_addr(uint8_t page)
{
    ssd1306_send_cmd(0xB0 | (page & 0x07));
}

static void ssd1306_set_column_addr(uint8_t col)
{
    uint8_t real_col = col + SSD1306_COL_OFFSET;
    ssd1306_send_cmd(0x00 | (real_col & 0x0F));
    ssd1306_send_cmd(0x10 | ((real_col >> 4) & 0x0F));
}

void ssd1306_init(i2c_master_bus_handle_t i2c_bus_handle)
{
    ssd1306_i2c_bus_handle = i2c_bus_handle;

    i2c_device_config_t dev_config = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address = SSD1306_I2C_ADDR,
        .scl_speed_hz = 100000,
    };

    ESP_ERROR_CHECK(i2c_master_bus_add_device(i2c_bus_handle, &dev_config, &ssd1306_dev_handle));

    for (size_t i = 0; i < sizeof(ssd1306_init_sequence); i++) {
        ssd1306_send_cmd(ssd1306_init_sequence[i]);
    }

    ssd1306_clear();
    ESP_LOGI(TAG, "SSD1306 initialized (64x32)");
}

void ssd1306_clear(void)
{
    // Clear all 128 internal columns to wipe any random power-up data
    for (uint8_t page = 0; page < SSD1306_MAX_PAGES; page++) {
        ssd1306_set_page_addr(page);
        ssd1306_set_column_addr(0);
        uint8_t empty[128] = {0};
        ssd1306_send_data(empty, sizeof(empty));
    }
}

void ssd1306_clear_line(uint8_t page)
{
    if (page >= SSD1306_MAX_PAGES) return;
    ssd1306_set_page_addr(page);
    ssd1306_set_column_addr(0);
    uint8_t empty[128] = {0};
    ssd1306_send_data(empty, sizeof(empty));
}

void ssd1306_set_cursor(uint8_t col, uint8_t page)
{
    if (col >= SSD1306_WIDTH || page >= SSD1306_MAX_PAGES) return;
    ssd1306_set_page_addr(page);
    ssd1306_set_column_addr(col);
}

