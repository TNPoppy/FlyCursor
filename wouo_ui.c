void UI_Init(void)
{
    memset(ui_buffer, 0, sizeof(ui_buffer));
}

void UI_Clear(void)
{
    memset(ui_buffer, 0, sizeof(ui_buffer));
}

void UI_SetPixel(uint8_t x, uint8_t y, UI_Color color)
{
    if (x >= UI_WIDTH || y >= UI_HEIGHT) return;
    uint8_t page = y / 8;
    uint8_t bit  = y % 8;
    if (color == UI_COLOR_WHITE) {
        ui_buffer[page][x] |= (1 << bit);
    } else if (color == UI_COLOR_BLACK) {
        ui_buffer[page][x] &= ~(1 << bit);
    } else if (color == UI_COLOR_XOR) {
        ui_buffer[page][x] ^= (1 << bit);
    }
}

void UI_DrawLine(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1, UI_Color color)
{
    int dx = abs((int)x1 - (int)x0);
    int dy = abs((int)y1 - (int)y0);
    int sx = (x0 < x1) ? 1 : -1;
    int sy = (y0 < y1) ? 1 : -1;
    int err = dx - dy;
    int x = x0;
    int y = y0;

    while (1) {
        UI_SetPixel((uint8_t)x, (uint8_t)y, color);
        if (x == x1 && y == y1) break;
        int e2 = 2 * err;
        if (e2 > -dy) { err -= dy; x += sx; }
        if (e2 < dx)  { err += dx; y += sy; }
    }
}

void UI_DrawRect(uint8_t x, uint8_t y, uint8_t w, uint8_t h, UI_Color color)
{
    if (w == 0 || h == 0) return;
    UI_DrawLine(x, y, x + w - 1, y, color);
    UI_DrawLine(x, y, x, y + h - 1, color);
    UI_DrawLine(x + w - 1, y, x + w - 1, y + h - 1, color);
    UI_DrawLine(x, y + h - 1, x + w - 1, y + h - 1, color);
}

void UI_FillRect(uint8_t x, uint8_t y, uint8_t w, uint8_t h, UI_Color color)
{
    for (uint8_t i = x; i < x + w && i < UI_WIDTH; i++) {
        for (uint8_t j = y; j < y + h && j < UI_HEIGHT; j++) {
            UI_SetPixel(i, j, color);
        }
    }
}

void UI_DrawCircle(uint8_t x, uint8_t y, uint8_t r, UI_Color color)
{
    int f = 1 - (int)r;
    int ddF_x = 1;
    int ddF_y = -2 * (int)r;
    int x0 = 0;
    int y0 = r;

    UI_SetPixel(x, y + r, color);
    UI_SetPixel(x, y - r, color);
    UI_SetPixel(x + r, y, color);
    UI_SetPixel(x - r, y, color);

    while (x0 < y0) {
        if (f >= 0) {
            y0--;
            ddF_y += 2;
            f += ddF_y;
        }
        x0++;
        ddF_x += 2;
        f += ddF_x;
        UI_SetPixel(x + x0, y + y0, color);
        UI_SetPixel(x - x0, y + y0, color);
        UI_SetPixel(x + x0, y - y0, color);
        UI_SetPixel(x - x0, y - y0, color);
        UI_SetPixel(x + y0, y + x0, color);
        UI_SetPixel(x - y0, y + x0, color);
        UI_SetPixel(x + y0, y - x0, color);
        UI_SetPixel(x - y0, y - x0, color);
    }
}

void UI_DrawString(uint8_t x, uint8_t y, const char *str)
{
    while (*str) {
        char c = *str++;
        if (c < ' ' || c > 'z') {
            x += 6;
            continue;
        }
        uint8_t idx = c - ' ';
        if (idx >= sizeof(font_5x8) / 5) {
            x += 6;
            continue;
        }
        for (uint8_t i = 0; i < 5; i++) {
            uint8_t col = font_5x8[idx * 5 + i];
            for (uint8_t bit = 0; bit < 8; bit++) {
                if (y + bit < UI_HEIGHT) {
                    UI_SetPixel(x + i, y + bit, (col >> bit) & 1);
                }
            }
        }
        x += 6; // 5px + 1px spacing
        if (x + 5 > UI_WIDTH) break;
    }
}

void UI_DrawBitmap(uint8_t x, uint8_t y, uint8_t w, uint8_t h, const uint8_t *bitmap)
{
    for (uint8_t i = 0; i < w && x + i < UI_WIDTH; i++) {
        for (uint8_t j = 0; j < h && y + j < UI_HEIGHT; j++) {
            uint8_t byte_idx = (j / 8) * w + i;
            uint8_t bit_idx  = j % 8;
            UI_SetPixel(x + i, y + j, (bitmap[byte_idx] >> bit_idx) & 1);
        }
    }
}

void UI_Refresh(void)
{
    for (uint8_t page = 0; page < 4; page++) {
        ssd1306_set_cursor(0, page);
        ssd1306_send_data(ui_buffer[page], 64);
    }
}
